    public void update(String type)
    {
        if(type=="Emprunts")
        {
               panEmprunt pe=new panEmprunt();
               this.remove(container);
                 //  pe.setPreferredSize(new Dimension(container.getWidth(),container.getHeight()));
              this.setContentPane(pe);
              pe.repaint();
              this.pack();
             
        }
        if(type=="LIVRES")
        {
            panLivres pl=new panLivres();
            this.remove(container);
           // pl.setPreferredSize(new Dimension(container.getWidth(),container.getHeight()));
            this.setContentPane(pl);
            pl.repaint();
            this.pack(); 
        }
        if(type=="RETOUR")
        {
            panRetour pr=new panRetour();
            this.remove(container);
            pr.setPreferredSize(new Dimension(container.getWidth(),container.getHeight()));
            this.setContentPane(pr);
            pr.repaint();
            this.pack();
        }
        if(type=="ADHERENTS")
        {
             panAdherent pa=new panAdherent();
        this.remove(container);
      //  pa.setPreferredSize(new Dimension(container.getWidth(),container.getHeight()));
        this.setContentPane(pa);
        pa.repaint();
        this.pack();
        }
        
        if(type=="DEBUTER")
        {
            BienDebuter bd=new BienDebuter();
            bd.setLocationRelativeTo(null);
            bd.setVisible(true);
        }
          
        if(type=="VISITE")
        {
            
        }
        
         System.out.print("ok");
    }
